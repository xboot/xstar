static void font_pure_init(void)
{
	struct font_ctx_t * ctx = &__font_ctx;

	truetype_init(&ctx->builtin.info, (unsigned char *)font_builtin, 0);
	ctx->builtin.priv = NULL;

	ctx->map[FONT_STYLE_REGULAR] = hmap_alloc(0, hmap_entry_callback);
	ctx->map[FONT_STYLE_ITALIC] = hmap_alloc(0, hmap_entry_callback);
	ctx->map[FONT_STYLE_BOLD] = hmap_alloc(0, hmap_entry_callback);
	ctx->map[FONT_STYLE_BOLDITALIC] = hmap_alloc(0, hmap_entry_callback);

	kobj_add_regular(search_class_font_kobj(), "regular", font_read_regular, NULL, ctx);
	kobj_add_regular(search_class_font_kobj(), "italic", font_read_italic, NULL, ctx);
	kobj_add_regular(search_class_font_kobj(), "bold", font_read_bold, NULL, ctx);
	kobj_add_regular(search_class_font_kobj(), "bolditalic", font_read_bolditalic, NULL, ctx);
}
pure_initcall(font_pure_init);
