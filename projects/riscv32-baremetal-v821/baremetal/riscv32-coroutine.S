/*
 *  -------------------------------------------------  *
 *  |  0  |  1  |  2  |  3  |  4  |  5  |  6  |  7  |  *
 *  -------------------------------------------------  *
 *  | 0x0 | 0x4 | 0x8 | 0xc | 0x10| 0x14| 0x18| 0x1c|  *
 *  -------------------------------------------------  *
 *  |    fs0    |    fs1    |    fs2    |    fs3    |  *
 *  -------------------------------------------------  *
 *  -------------------------------------------------  *
 *  |  8  |  9  |  10 |  11 |  12 |  13 |  14 |  15 |  *
 *  -------------------------------------------------  *
 *  | 0x20| 0x24| 0x28| 0x2c| 0x30| 0x34| 0x38| 0x3c|  *
 *  -------------------------------------------------  *
 *  |    fs4    |    fs5    |    fs6    |    fs7    |  *
 *  -------------------------------------------------  *
 *  -------------------------------------------------  *
 *  |  16 |  17 |  18 |  19 |  20 |  21 |  22 |  23 |  *
 *  -------------------------------------------------  *
 *  | 0x40| 0x44| 0x48| 0x4c| 0x50| 0x54| 0x58| 0x5c|  *
 *  -------------------------------------------------  *
 *  |    fs8    |    fs9    |    fs10   |    fs11   |  *
 *  -------------------------------------------------  *
 *  -------------------------------------------------  *
 *  |  24 |  25 |  26 |  27 |  28 |  29 |  30 |  31 |  *
 *  -------------------------------------------------  *
 *  | 0x60| 0x64| 0x68| 0x6c| 0x70| 0x74| 0x78| 0x7c|  *
 *  -------------------------------------------------  *
 *  | s0  | s1  | s2  | s3  | s4  | s5  | s6  | s7  |  *
 *  -------------------------------------------------  *
 *  -------------------------------------------------  *
 *  |  32 |  33 |  34 |  35 |  36 |  37 |  38 |  39 |  *
 *  -------------------------------------------------  *
 *  | 0x80| 0x84| 0x88| 0x8c| 0x90| 0x94|     |     |  *
 *  -------------------------------------------------  *
 *  | s8  | s9  | s10 | s11 | ra  | pc  |     |     |  *
 *  -------------------------------------------------  *
 */
	.text
	.global riscv32_coroutine_make
	.align 3
	.type riscv32_coroutine_make, %function
riscv32_coroutine_make:
	/* shift address in a0 (allocated stack) to lower 16 byte boundary */
	andi a0, a0, ~0xf

	/* reserve space for context-data on context-stack */
	addi a0, a0, -0x98

	/* third arg of riscv32_coroutine_make() == address of context-function */
	/* store address as a pc to jump in */
	sw a2, 0x94(a0)

	/* save address of finish as return-address for context-function */
	/* will be entered after context-function returns (ra register) */
	la a4, finish
	sw a4, 0x90(a0)

	/* return pointer to context-data (a0) */
	ret

finish:
	/* exit code is zero */
	li a0, 0
	/* exit application */
	tail exit

/*
 *  -------------------------------------------------  *
 *  |  0  |  1  |  2  |  3  |  4  |  5  |  6  |  7  |  *
 *  -------------------------------------------------  *
 *  | 0x0 | 0x4 | 0x8 | 0xc | 0x10| 0x14| 0x18| 0x1c|  *
 *  -------------------------------------------------  *
 *  |    fs0    |    fs1    |    fs2    |    fs3    |  *
 *  -------------------------------------------------  *
 *  -------------------------------------------------  *
 *  |  8  |  9  |  10 |  11 |  12 |  13 |  14 |  15 |  *
 *  -------------------------------------------------  *
 *  | 0x20| 0x24| 0x28| 0x2c| 0x30| 0x34| 0x38| 0x3c|  *
 *  -------------------------------------------------  *
 *  |    fs4    |    fs5    |    fs6    |    fs7    |  *
 *  -------------------------------------------------  *
 *  -------------------------------------------------  *
 *  |  16 |  17 |  18 |  19 |  20 |  21 |  22 |  23 |  *
 *  -------------------------------------------------  *
 *  | 0x40| 0x44| 0x48| 0x4c| 0x50| 0x54| 0x58| 0x5c|  *
 *  -------------------------------------------------  *
 *  |    fs8    |    fs9    |    fs10   |    fs11   |  *
 *  -------------------------------------------------  *
 *  -------------------------------------------------  *
 *  |  24 |  25 |  26 |  27 |  28 |  29 |  30 |  31 |  *
 *  -------------------------------------------------  *
 *  | 0x60| 0x64| 0x68| 0x6c| 0x70| 0x74| 0x78| 0x7c|  *
 *  -------------------------------------------------  *
 *  | s0  | s1  | s2  | s3  | s4  | s5  | s6  | s7  |  *
 *  -------------------------------------------------  *
 *  -------------------------------------------------  *
 *  |  32 |  33 |  34 |  35 |  36 |  37 |  38 |  39 |  *
 *  -------------------------------------------------  *
 *  | 0x80| 0x84| 0x88| 0x8c| 0x90| 0x94|     |     |  *
 *  -------------------------------------------------  *
 *  | s8  | s9  | s10 | s11 | ra  | pc  |     |     |  *
 *  -------------------------------------------------  *
 */
	.text
	.global riscv32_coroutine_jump
	.align 3
	.type riscv32_coroutine_jump, %function
riscv32_coroutine_jump:
	/* prepare stack */
	addi sp, sp, -(38 * 4)

	/* save fs0 - fs11 */
#if __riscv_flen == 64
	fsd fs0, 0x00(sp)
	fsd fs1, 0x08(sp)
	fsd fs2, 0x10(sp)
	fsd fs3, 0x18(sp)
	fsd fs4, 0x20(sp)
	fsd fs5, 0x28(sp)
	fsd fs6, 0x30(sp)
	fsd fs7, 0x38(sp)
	fsd fs8, 0x40(sp)
	fsd fs9, 0x48(sp)
	fsd fs10, 0x50(sp)
	fsd fs11, 0x58(sp)
#endif

	/* save s0 - s11, ra */
	sw s0, 0x60(sp)
	sw s1, 0x64(sp)
	sw s2, 0x68(sp)
	sw s3, 0x6c(sp)
	sw s4, 0x70(sp)
	sw s5, 0x74(sp)
	sw s6, 0x78(sp)
	sw s7, 0x7c(sp)
	sw s8, 0x80(sp)
	sw s9, 0x84(sp)
	sw s10, 0x88(sp)
	sw s11, 0x8c(sp)
	sw ra, 0x90(sp)

	/* save ra as pc */
	sw ra, 0x94(sp)

	/* store sp (pointing to context-data) in a2 */
	mv a2, sp

	/* restore sp (pointing to context-data) from a0 */
	mv sp, a0

	/* load fs0 - fs11 */
#if __riscv_flen == 64
	fld fs0, 0x00(sp)
	fld fs1, 0x08(sp)
	fld fs2, 0x10(sp)
	fld fs3, 0x18(sp)
	fld fs4, 0x20(sp)
	fld fs5, 0x28(sp)
	fld fs6, 0x30(sp)
	fld fs7, 0x38(sp)
	fld fs8, 0x40(sp)
	fld fs9, 0x48(sp)
	fld fs10, 0x50(sp)
	fld fs11, 0x58(sp)
#endif

	/* load s0 - s11 */
	lw s0, 0x60(sp)
	lw s1, 0x64(sp)
	lw s2, 0x68(sp)
	lw s3, 0x6c(sp)
	lw s4, 0x70(sp)
	lw s5, 0x74(sp)
	lw s6, 0x78(sp)
	lw s7, 0x7c(sp)
	lw s8, 0x80(sp)
	lw s9, 0x84(sp)
	lw s10, 0x88(sp)
	lw s11, 0x8c(sp)
	lw ra, 0x90(sp)

	/* return transfer_t from jump */
	/* pass transfer_t as first arg in context function */
	mv a0, a2

	/* load pc */
	lw a2, 0x94(sp)

	/* restore stack */
	addi sp, sp, 0x98

	jr a2
